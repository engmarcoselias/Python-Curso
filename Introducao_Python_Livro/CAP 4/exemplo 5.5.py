'''Programa que conta de 1 ate 3 e imprime o valor na tela'''

x = 1         # Atribui valor a varial
while x <= 3: # condição para repetição  ( o bloco sempre vai repetir enquanto a condição for TRUE 
    print(x)  # imprime o valor na tela
    x = x + 1 # soma 1 ao valor de x e depois volta a condição
